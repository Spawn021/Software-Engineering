package project.UI;

public class Topping {
    private String anhTopping;
    private String thongTinTopping;
    private String giaTopping;
    private boolean toppingOnMenu;

    public String getAnhTopping() {
        return anhTopping;
    }

    public void setAnhTopping(String anhTopping) {
        this.anhTopping = anhTopping;
    }

    public String getThongTinTopping() {
        return thongTinTopping;
    }

    public void setThongTinTopping(String thongTinTopping) {
        this.thongTinTopping = thongTinTopping;
    }

    public String getGiaTopping() {
        return giaTopping;
    }

    public void setGiaTopping(String giaTopping) {
        this.giaTopping = giaTopping;
    }

    public boolean getToppingOnMenu() {
        return toppingOnMenu;
    }

    public void setToppingOnMenu(boolean toppingOnMenu) {
        this.toppingOnMenu = toppingOnMenu;
    }
}
